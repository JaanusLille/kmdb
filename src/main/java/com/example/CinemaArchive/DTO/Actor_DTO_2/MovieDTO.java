package com.example.CinemaArchive.DTO.Actor_DTO_2;


public class MovieDTO {
    private Long id;
    private String name;

    // Constructor
    public MovieDTO(Long id, String name) {
        this.id = id;
        this.name = name;
    }

    // Getters and Setters
    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
